namespace Shift.Constant
{
    public enum ConsequenceType
    {
        /// <summary>
        /// A low-stakes exam involves the use of evaluation instruments that have little or no impact on decisions.
        /// </summary>
        Low,

        /// <summary>
        /// A medium-stakes exam has some impact on decisions about a student/candidate.
        /// </summary>
        Medium,

        /// <summary>
        /// A high-stakes exam is one that has major consequences or is the basis of a major decision.
        /// </summary>
        High
    }
}