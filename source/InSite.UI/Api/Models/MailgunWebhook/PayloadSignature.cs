using System;
using System.Security.Cryptography;
using System.Text;

using Newtonsoft.Json;

using Shift.Common;

namespace InSite.Api.Models.MailgunWebhook
{
    public sealed class PayloadSignature
    {
        /// <summary>
        /// Number of seconds passed since January 1, 1970.
        /// </summary>
        [JsonProperty(PropertyName = "timestamp")]
        public string Timestamp { get; private set; }

        /// <summary>
        /// Randomly generated string with length of 50.
        /// </summary>
        [JsonProperty(PropertyName = "token")]
        public string Token { get; private set; }

        /// <summary>
        /// String with hexadecimal digits generated by an HMAC algorithm
        /// </summary>
        [JsonProperty(PropertyName = "signature")]
        public string Signature { get; private set; }

        [JsonConstructor]
        private PayloadSignature()
        {

        }

        public bool IsValid(string signingKey)
        {
            if (Timestamp.IsEmpty() || Token.IsEmpty() || Signature.IsEmpty())
                return false;

            using (var hmac = new HMACSHA256(Encoding.UTF8.GetBytes(signingKey)))
            {
                var hashBytes = hmac.ComputeHash(Encoding.UTF8.GetBytes(Timestamp + Token));
                var hashHex = StringHelper.ByteArrayToHex(hashBytes);

                return string.Equals(hashHex, Signature, StringComparison.OrdinalIgnoreCase);
            }
        }

        public bool TryGetTimestamp(out DateTime result)
        {
            if (long.TryParse(Timestamp, out var unixSeconds))
            {
                result = Clock.FromUnixTimestamp(unixSeconds);
                return true;
            }
            else
            {
                result = DateTime.MinValue;
                return false;
            }
        }
    }
}